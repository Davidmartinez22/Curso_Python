###Operadores Aritmeticos###

# Operaciones de Enteros
# Suma de dos numeros

numero1 = 2
numero2 = 4
print("Suma de dos numeros")
print(numero1+numero2)

#resta
print(numero1-numero2)

#Modulo
print(numero1%numero2)

#Division Entera sin Decimales
print(numero1//numero2)

#Exponente
print(numero1**numero2)

#Combinar operaciones
print(2**3-7/1//4)

#Operaciones con texto
